#!/usr/bin/env node
// Single source of truth for redirects: .vitepress/theme/redirects.ts
//
// This script fans that source out to:
//   1. .vitepress/_redirects.nginx  — snippet consumed by the nginx build
//      in the release-docs repo. Always written (not tracked by git).
//   2. vercel.json (redirects field) — consumed by Vercel preview/production.
//      Only written when running in a real build environment (CI / VERCEL /
//      RELEASE_BUILD), so local `npm run build` / `npm run dev` never dirty
//      the working tree.
//
// The `headers` field and any other keys in vercel.json are preserved.

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const docsRoot = path.resolve(__dirname, '..', '..');

const redirectsTs = path.join(docsRoot, '.vitepress', 'theme', 'redirects.ts');
const vercelJson = path.join(docsRoot, 'vercel.json');
const nginxOut = path.join(docsRoot, '.vitepress', '_redirects.nginx');

function parseNamedExport(src, exportName) {
  const marker = `export const ${exportName}`;
  const start = src.indexOf(marker);
  if (start === -1) return [];
  const openBrace = src.indexOf('{', start);
  // find the matching closing brace
  let depth = 0;
  let closeBrace = -1;
  for (let i = openBrace; i < src.length; i++) {
    if (src[i] === '{') depth++;
    else if (src[i] === '}') { depth--; if (depth === 0) { closeBrace = i; break; } }
  }
  if (openBrace === -1 || closeBrace === -1) {
    throw new Error(`redirects.ts: cannot locate object literal for '${exportName}'`);
  }
  const body = src.slice(openBrace + 1, closeBrace);
  const entries = [];
  const re = /(['"])([^'"]+)\1\s*:\s*(['"])([^'"]+)\3/g;
  let m;
  while ((m = re.exec(body)) !== null) {
    entries.push([m[2], m[4]]);
  }
  return entries;
}

const src = fs.readFileSync(redirectsTs, 'utf8');
const permanentEntries = parseNamedExport(src, 'redirects');
const temporaryEntries = parseNamedExport(src, 'temporaryRedirects');
const allEntries = permanentEntries.length + temporaryEntries.length;

if (allEntries === 0) {
  console.warn('[sync-redirects] no entries parsed from redirects.ts');
}

// Guard against redirect chains (A -> B, then B -> C). Every hop costs a
// round trip and bleeds SEO equity; Google stops following after a few
// hops. When the destination of a rule is itself the source of another
// rule, flatten the earlier rule to point at the final target instead.
const sources = new Set([
  ...permanentEntries.map(([from]) => from),
  ...temporaryEntries.map(([from]) => from),
]);
const chains = [...permanentEntries, ...temporaryEntries].filter(
  ([, to]) => sources.has(to.replace(/\/$/, '')) || sources.has(to)
);
if (chains.length > 0) {
  console.error('[sync-redirects] redirect chain(s) detected — flatten these in redirects.ts:');
  for (const [from, to] of chains) {
    console.error(`  ${from} -> ${to} (and ${to} is itself a redirect source)`);
  }
  process.exit(1);
}

const nginxLines = [
  ...permanentEntries.map(([from, to]) => `location = ${from} { return 301 ${to}; }`),
  ...temporaryEntries.map(([from, to]) => `location = ${from} { return 302 ${to}; }`),
];
fs.mkdirSync(path.dirname(nginxOut), { recursive: true });
fs.writeFileSync(
  nginxOut,
  `# generated by .vitepress/scripts/sync-redirects.mjs — do not edit by hand\n${nginxLines.join('\n')}\n`
);
console.log(
  `[sync-redirects] wrote ${permanentEntries.length} permanent + ${temporaryEntries.length} temporary rules -> ${path.relative(docsRoot, nginxOut)}`
);

const inBuildEnv = !!(
  process.env.CI ||
  process.env.VERCEL ||
  process.env.RELEASE_BUILD
);

if (inBuildEnv) {
  const raw = fs.readFileSync(vercelJson, 'utf8');
  const cfg = JSON.parse(raw);
  cfg.redirects = [
    ...permanentEntries.map(([from, to]) => ({ source: from, destination: to, permanent: true })),
    ...temporaryEntries.map(([from, to]) => ({ source: from, destination: to, permanent: false })),
  ];
  fs.writeFileSync(vercelJson, JSON.stringify(cfg, null, 2) + '\n');
  console.log(
    `[sync-redirects] updated vercel.json with ${permanentEntries.length} permanent + ${temporaryEntries.length} temporary redirects (build env detected)`
  );
} else {
  console.log(
    '[sync-redirects] skipped vercel.json (local run; set CI=1, VERCEL=1, or RELEASE_BUILD=1 to enable)'
  );
}
