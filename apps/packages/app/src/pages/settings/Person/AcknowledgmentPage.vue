const searchContent = ref<string>('');
const searchBackerList = computed(() => {
	if (!searchContent.value) {
		return backerList.value
			? [...backerList.value].sort((a, b) => a.number - b.number)
			: [];
	}

	const keyword = searchContent.value.trim().toLowerCase();
	const rawKeyword = searchContent.value.trim();
	const result: Array<{ item: Backer; weight: number }> = [];

	backerList.value?.forEach((item) => {
		let weight = 0;

		if (item.name.toLowerCase().trim() === keyword) {
			weight = 1003;
		} else if (item.uid && String(item.uid).trim() === rawKeyword) {
			weight = 1002;
		} else if (String(item.number) === rawKeyword) {
			weight = 1001;
		} else {
			if (item.name.toLowerCase().trim().includes(keyword)) {
				weight = 3;
			} else if (item.uid && String(item.uid).trim().includes(rawKeyword)) {
				weight = 2;
			} else if (String(item.number).includes(rawKeyword)) {
				weight = 1;
			}
		}

		if (weight > 0) {
			result.push({ item, weight });
		}
	});

	return result
		.sort((a, b) => {
			if (b.weight !== a.weight) {
				return b.weight - a.weight;
			}
			return a.item.number - b.item.number;
		})
		.map((item) => item.item);
});
